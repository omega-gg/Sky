#!/bin/sh
set -e

#--------------------------------------------------------------------------------------------------
# Settings
#--------------------------------------------------------------------------------------------------

root="$PWD"

ffmpeg="$PWD/../ffmpeg"

#--------------------------------------------------------------------------------------------------
# Syntax
#--------------------------------------------------------------------------------------------------

if [ $# -lt 3 -o $# -gt 4 ] || [ $# = 4 -a "$4" != "lossless" ]; then

    echo "Usage: reframeWide <image> <video> <output> [lossless]"
    echo ""
    echo "This command output is usefull to turn 16:9 into 21:9 with Luma reframe."

    exit 1
fi

#--------------------------------------------------------------------------------------------------
# Run
#--------------------------------------------------------------------------------------------------

echo "----------"
echo "GENERATING"
echo "----------"

cd "$ffmpeg"

sh concatZoom.sh "$1" "$2" "$root/reframe.mp4"

cd -

echo "---------"
echo "REFRAMING"
echo "---------"

sh reframe.sh reframe.mp4 temp.mp4 21:9

rm reframe.mp4

echo "--------"
echo "RESIZING"
echo "--------"

cd "$ffmpeg"

if [ "$4" = "lossless" ]; then

    sh resize.sh "$root/temp.mp4" "$2" "$3" 1 0 lossless
else
    sh resize.sh "$root/temp.mp4" "$2" "$3" 1
fi

cd -

#--------------------------------------------------------------------------------------------------
# Clean
#--------------------------------------------------------------------------------------------------

rm temp.mp4

#!/bin/sh
set -e

#--------------------------------------------------------------------------------------------------
# Settings
#--------------------------------------------------------------------------------------------------

root="$PWD"

ffmpeg="$PWD/bin/ffmpeg"

#--------------------------------------------------------------------------------------------------
# Syntax
#--------------------------------------------------------------------------------------------------

if [ $# -lt 3 -o $# -gt 4 ] || [ $# = 4 -a "$4" != "lossless" ]; then

    echo "Usage: reframeWide <image> <video> <output> [lossless]"
    echo ""
    echo "This command output is usefull to turn 16:9 into 21:9 with Luma reframe."

    exit 1
fi

#--------------------------------------------------------------------------------------------------
# Run
#--------------------------------------------------------------------------------------------------

cd "$ffmpeg"

sh concatZoom.sh "$1" "$2" "$PWD/reframe.mp4"

cd -

sh reframe.sh reframe.mp4 temp.mp4 21:9

rm reframe.mp4

if [ "$4" = "lossless" ]; then

    sh resize.sh temp.mp4 "$2" "$3" 1 0 lossless
else
    sh resize.sh temp.mp4 "$2" "$3" 1
fi

#--------------------------------------------------------------------------------------------------
# Clean
#--------------------------------------------------------------------------------------------------

rm temp.mp4
